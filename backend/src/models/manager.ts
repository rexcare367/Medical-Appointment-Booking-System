import mongoose from 'mongoose';

const Scheme = mongoose.Schema;

let Manager = new Scheme({
    _id: {
        type: String,
        required: true
    },
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    }
});

export default mongoose.model('Manager', Manager, 'managers');
